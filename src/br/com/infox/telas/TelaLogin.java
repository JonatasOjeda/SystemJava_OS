    public void Logar() {
        String sql = "select * from tbusuarios where login =? and senha=?";
        try {
            //As linhas abaixo preparam a consulta ao banco em função do
            //Que foi Digitado nas caixas de texto. O "?" é substituido pelo 
            //conteudo das Variaveis
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtUsuario.getText());
            pst.setString(2, txtSenha.getText());
            //As linhas abaixa executa a Query
            rs = pst.executeQuery();
            //Se existir usuario e senha correspondente
            if (rs.next()) {
                // A Linha Abaixo obtem o Conteudo do Campo Perfil da Tabela tbusuarios
                String perfil = rs.getString(6);
                //System.out.println(perfil);
                // A Estrutura abaixo faz o tratamento do perfil do usuario
                if (perfil.equals("admin")) {
                    TelaPrincipal principal = new TelaPrincipal();
                    principal.setVisible(true);
                    TelaPrincipal.menRel.setEnabled(true);
                    TelaPrincipal.menCadUser.setEnabled(true);
                    TelaPrincipal.lblUser.setText(rs.getString(2));
                    TelaPrincipal.lblUser.setForeground(Color.red);
                    this.dispose();
                    conexao.close();
                } else {
                    TelaPrincipal principal = new TelaPrincipal();
                    principal.setVisible(true);
                    TelaPrincipal.lblUser.setText(rs.getString(2));
                    this.dispose();
                }

            } else {
                JOptionPane.showMessageDialog(null, "Usuario e/ou Senha Invalido(s)");

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

